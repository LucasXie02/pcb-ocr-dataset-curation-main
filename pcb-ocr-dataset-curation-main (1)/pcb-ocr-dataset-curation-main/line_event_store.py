#!/usr/bin/env python3
"""
Line Event Store
================
Manages line-level event logging for the OCR review system.

Events track the lifecycle of each line annotation:
- PROPOSED: Line generated by fusion tool
- ACCEPTED: Line auto-accepted (k == len(S))
- UNCERTAIN: Line flagged for review
- REVIEWED: Human reviewed the line (accept/edit)
- EDITED: Human modified line or characters
- DELETED: Line deleted by reviewer

Event Schema:
    - image_id: str (e.g., "17_0_Resistor")
    - line_uid: str (e.g., "17_0_Resistor#L0")
    - timestamp: ISO format datetime
    - event_type: str (PROPOSED, ACCEPTED, UNCERTAIN, REVIEWED, EDITED, DELETED)
    - data: JSON object with event-specific data

Backend: SQLite for fast queries and simple deployment
"""

import json
import sqlite3
from datetime import datetime
from pathlib import Path
from typing import Dict, List, Optional, Any
from dataclasses import dataclass, asdict
from enum import Enum


class EventType(Enum):
    """Event types for line lifecycle tracking"""
    PROPOSED = "PROPOSED"           # Line generated by fusion tool
    ACCEPTED = "ACCEPTED"           # Auto-accepted by fusion tool
    UNCERTAIN = "UNCERTAIN"         # Flagged for review (LEN_MISMATCH, etc.)
    REVIEWED = "REVIEWED"           # Human reviewed (A key)
    EDITED = "EDITED"              # Human edited line/chars (E key)
    SKIPPED = "SKIPPED"            # Deprecated: human skipped (X key)
    DELETED = "DELETED"            # Line deleted (remove line-bbox and chars)
    GROUP_ACCEPTED = "GROUP_ACCEPTED"                   # All siblings accepted at once
    GROUP_MAJORITY_ACCEPTED = "GROUP_MAJORITY_ACCEPTED" # Majority accepted, outliers rejected
    INSTANCE_REJECTED = "INSTANCE_REJECTED"             # Specific instance rejected (defective)


@dataclass
class LineEvent:
    """Represents a single event in a line's lifecycle"""
    image_id: str
    line_uid: str
    timestamp: str
    event_type: str
    data: Dict[str, Any]
    event_id: Optional[int] = None
    region_group_id: str = ""

    def to_dict(self) -> Dict:
        """Convert to dictionary"""
        return asdict(self)

    @classmethod
    def from_row(cls, row: tuple) -> 'LineEvent':
        """Create from database row (handles both 6-column and 7-column rows)"""
        if len(row) >= 7:
            event_id, image_id, line_uid, timestamp, event_type, data_json, region_group_id = row[:7]
        else:
            event_id, image_id, line_uid, timestamp, event_type, data_json = row[:6]
            region_group_id = ""
        data = json.loads(data_json) if data_json else {}
        return cls(
            event_id=event_id,
            image_id=image_id,
            line_uid=line_uid,
            timestamp=timestamp,
            event_type=event_type,
            data=data,
            region_group_id=region_group_id or ""
        )


class LineEventStore:
    """
    SQLite-backed event store for line-level tracking.

    Usage:
        store = LineEventStore("path/to/review.db")

        # Log an event
        store.log_event("img001", "img001#L0", EventType.ACCEPTED, {
            "ocr_text": "R5C",
            "char_count": 3
        })

        # Query events
        events = store.get_line_events("img001#L0")

        # Get line status
        status = store.get_line_status("img001#L0")
    """

    def __init__(self, db_path: Path):
        """
        Initialize event store.

        Args:
            db_path: Path to SQLite database file
        """
        self.db_path = Path(db_path)
        self.db_path.parent.mkdir(parents=True, exist_ok=True)
        self._init_db()

    def _init_db(self):
        """Initialize database schema"""
        with sqlite3.connect(self.db_path) as conn:
            cursor = conn.cursor()

            # Events table
            cursor.execute("""
                CREATE TABLE IF NOT EXISTS events (
                    event_id INTEGER PRIMARY KEY AUTOINCREMENT,
                    image_id TEXT NOT NULL,
                    line_uid TEXT NOT NULL,
                    timestamp TEXT NOT NULL,
                    event_type TEXT NOT NULL,
                    data TEXT,
                    UNIQUE(line_uid, timestamp)
                )
            """)

            # Indexes for fast queries
            cursor.execute("""
                CREATE INDEX IF NOT EXISTS idx_line_uid ON events(line_uid)
            """)

            cursor.execute("""
                CREATE INDEX IF NOT EXISTS idx_image_id ON events(image_id)
            """)

            cursor.execute("""
                CREATE INDEX IF NOT EXISTS idx_event_type ON events(event_type)
            """)

            # Migration: add region_group_id column if missing
            try:
                cursor.execute("ALTER TABLE events ADD COLUMN region_group_id TEXT DEFAULT ''")
            except sqlite3.OperationalError:
                pass  # Column already exists

            cursor.execute("CREATE INDEX IF NOT EXISTS idx_region_group_id ON events(region_group_id)")

            conn.commit()

    def log_event(self, image_id: str, line_uid: str, event_type: EventType,
                  data: Optional[Dict[str, Any]] = None,
                  region_group_id: str = "") -> int:
        """
        Log a new event.

        Args:
            image_id: Image identifier
            line_uid: Line unique identifier
            event_type: Type of event
            data: Event-specific data
            region_group_id: Optional component group identifier

        Returns:
            Event ID
        """
        with sqlite3.connect(self.db_path) as conn:
            cursor = conn.cursor()

            timestamp = datetime.now().isoformat()
            data_json = json.dumps(data) if data else "{}"

            cursor.execute("""
                INSERT INTO events (image_id, line_uid, timestamp, event_type, data, region_group_id)
                VALUES (?, ?, ?, ?, ?, ?)
            """, (image_id, line_uid, timestamp, event_type.value, data_json, region_group_id))

            event_id = cursor.lastrowid
            conn.commit()

            return event_id

    def get_line_events(self, line_uid: str) -> List[LineEvent]:
        """
        Get all events for a specific line.

        Args:
            line_uid: Line unique identifier

        Returns:
            List of events ordered by timestamp
        """
        with sqlite3.connect(self.db_path) as conn:
            cursor = conn.cursor()

            cursor.execute("""
                SELECT event_id, image_id, line_uid, timestamp, event_type, data, region_group_id
                FROM events
                WHERE line_uid = ?
                ORDER BY timestamp ASC
            """, (line_uid,))

            events = [LineEvent.from_row(row) for row in cursor.fetchall()]

            return events

    def get_image_events(self, image_id: str) -> List[LineEvent]:
        """
        Get all events for a specific image.

        Args:
            image_id: Image identifier

        Returns:
            List of events ordered by timestamp
        """
        with sqlite3.connect(self.db_path) as conn:
            cursor = conn.cursor()

            cursor.execute("""
                SELECT event_id, image_id, line_uid, timestamp, event_type, data, region_group_id
                FROM events
                WHERE image_id = ?
                ORDER BY timestamp ASC
            """, (image_id,))

            events = [LineEvent.from_row(row) for row in cursor.fetchall()]

            return events

    def get_line_status(self, line_uid: str) -> Dict[str, Any]:
        """
        Get current status of a line based on its event history.

        Status determination:
        - If DELETED event exists: status = "deleted"
        - If REVIEWED event exists: status = "reviewed"
        - If EDITED event exists: status = "edited"
        - If SKIPPED event exists: status = "deleted" (backward compatibility)
        - If ACCEPTED event exists: status = "accepted"
        - If UNCERTAIN event exists: status = "uncertain"
        - Otherwise: status = "proposed"

        Args:
            line_uid: Line unique identifier

        Returns:
            Dictionary with status info:
            {
                "status": str,
                "last_event_type": str,
                "last_event_time": str,
                "review_count": int,
                "edit_count": int,
                "has_manual_edits": bool
            }
        """
        events = self.get_line_events(line_uid)

        if not events:
            return {
                "status": "unknown",
                "last_event_type": None,
                "last_event_time": None,
                "review_count": 0,
                "edit_count": 0,
                "has_manual_edits": False
            }

        # Count event types
        event_counts = {}
        for event in events:
            event_counts[event.event_type] = event_counts.get(event.event_type, 0) + 1

        # Determine status (priority order)
        status = "proposed"
        if EventType.DELETED.value in event_counts:
            status = "deleted"
        elif EventType.SKIPPED.value in event_counts:
            status = "deleted"
        elif EventType.EDITED.value in event_counts:
            status = "edited"
        elif EventType.REVIEWED.value in event_counts:
            status = "reviewed"
        elif EventType.ACCEPTED.value in event_counts:
            status = "accepted"
        elif EventType.UNCERTAIN.value in event_counts:
            status = "uncertain"

        last_event = events[-1]

        return {
            "status": status,
            "last_event_type": last_event.event_type,
            "last_event_time": last_event.timestamp,
            "review_count": event_counts.get(EventType.REVIEWED.value, 0),
            "edit_count": event_counts.get(EventType.EDITED.value, 0),
            "has_manual_edits": EventType.EDITED.value in event_counts
        }

    def get_all_line_uids(self) -> List[str]:
        """
        Get all unique line UIDs in the database.

        Returns:
            List of line UIDs
        """
        with sqlite3.connect(self.db_path) as conn:
            cursor = conn.cursor()

            cursor.execute("""
                SELECT DISTINCT line_uid
                FROM events
                ORDER BY line_uid
            """)

            line_uids = [row[0] for row in cursor.fetchall()]

            return line_uids

    def get_lines_by_status(self, status: str) -> List[str]:
        """
        Get all line UIDs with a specific status.

        Args:
            status: Status filter (accepted, uncertain, reviewed, edited, deleted)

        Returns:
            List of line UIDs
        """
        all_uids = self.get_all_line_uids()
        filtered = []

        for uid in all_uids:
            line_status = self.get_line_status(uid)
            if line_status["status"] == status:
                filtered.append(uid)

        return filtered

    def get_statistics(self) -> Dict[str, Any]:
        """
        Get overall statistics across all lines.

        Returns:
            Dictionary with statistics:
            {
                "total_lines": int,
                "by_status": {"accepted": N, "uncertain": N, ...},
                "total_events": int,
                "by_event_type": {"ACCEPTED": N, "REVIEWED": N, ...}
            }
        """
        with sqlite3.connect(self.db_path) as conn:
            cursor = conn.cursor()

            # Total lines
            cursor.execute("SELECT COUNT(DISTINCT line_uid) FROM events")
            total_lines = cursor.fetchone()[0]

            # Total events
            cursor.execute("SELECT COUNT(*) FROM events")
            total_events = cursor.fetchone()[0]

            # Events by type
            cursor.execute("""
                SELECT event_type, COUNT(*)
                FROM events
                GROUP BY event_type
            """)
            by_event_type = {row[0]: row[1] for row in cursor.fetchall()}

        # Lines by status (requires computing status for each line)
        all_uids = self.get_all_line_uids()
        by_status = {}
        for uid in all_uids:
            status = self.get_line_status(uid)["status"]
            by_status[status] = by_status.get(status, 0) + 1

        return {
            "total_lines": total_lines,
            "by_status": by_status,
            "total_events": total_events,
            "by_event_type": by_event_type
        }

    def get_group_events(self, region_group_id: str) -> List[LineEvent]:
        """
        Get all events for a specific component group.

        Args:
            region_group_id: Component group identifier

        Returns:
            List of events ordered by timestamp
        """
        with sqlite3.connect(self.db_path) as conn:
            cursor = conn.cursor()

            cursor.execute("""
                SELECT event_id, image_id, line_uid, timestamp, event_type, data, region_group_id
                FROM events
                WHERE region_group_id = ?
                ORDER BY timestamp ASC
            """, (region_group_id,))

            return [LineEvent.from_row(row) for row in cursor.fetchall()]

    def log_group_event(self, region_group_id: str, event_type: EventType,
                        line_uids: List[str], data: Optional[Dict[str, Any]] = None) -> List[int]:
        """
        Log the same event for all lines in a group at once.

        Args:
            region_group_id: Component group identifier
            event_type: Type of event
            line_uids: List of line UIDs in the group
            data: Event-specific data (shared across all lines)

        Returns:
            List of event IDs
        """
        event_ids = []
        with sqlite3.connect(self.db_path) as conn:
            cursor = conn.cursor()
            timestamp = datetime.now().isoformat()
            data_json = json.dumps(data) if data else "{}"

            for line_uid in line_uids:
                # Extract image_id from line_uid (everything before #)
                image_id = line_uid.split("#")[0] if "#" in line_uid else line_uid

                cursor.execute("""
                    INSERT INTO events (image_id, line_uid, timestamp, event_type, data, region_group_id)
                    VALUES (?, ?, ?, ?, ?, ?)
                """, (image_id, line_uid, timestamp, event_type.value, data_json, region_group_id))

                event_ids.append(cursor.lastrowid)

            conn.commit()

        return event_ids

    def get_group_status(self, region_group_id: str) -> Dict[str, Any]:
        """
        Get aggregate status for a component group.

        Returns:
            Dictionary with group-level status info:
            {
                "region_group_id": str,
                "line_count": int,
                "line_statuses": {line_uid: status_dict, ...},
                "status_summary": {"accepted": N, "uncertain": N, ...},
                "fully_reviewed": bool
            }
        """
        events = self.get_group_events(region_group_id)

        # Collect unique line UIDs from group events
        line_uids = sorted(set(e.line_uid for e in events))

        line_statuses = {}
        status_summary: Dict[str, int] = {}
        for uid in line_uids:
            status_info = self.get_line_status(uid)
            line_statuses[uid] = status_info
            s = status_info["status"]
            status_summary[s] = status_summary.get(s, 0) + 1

        terminal_statuses = {"reviewed", "edited", "deleted", "accepted"}
        fully_reviewed = all(
            line_statuses[uid]["status"] in terminal_statuses
            for uid in line_uids
        ) if line_uids else False

        return {
            "region_group_id": region_group_id,
            "line_count": len(line_uids),
            "line_statuses": line_statuses,
            "status_summary": status_summary,
            "fully_reviewed": fully_reviewed,
        }

    def clear_all(self):
        """Clear all events (for testing)"""
        with sqlite3.connect(self.db_path) as conn:
            cursor = conn.cursor()
            cursor.execute("DELETE FROM events")
            conn.commit()


# ============================================================================
# Helper Functions
# ============================================================================

def create_event_store(db_path: Path) -> LineEventStore:
    """
    Create or open an event store.

    Args:
        db_path: Path to SQLite database

    Returns:
        LineEventStore instance
    """
    return LineEventStore(db_path)
